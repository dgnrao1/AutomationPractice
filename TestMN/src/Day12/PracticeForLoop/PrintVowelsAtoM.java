package Day12.PracticeForLoop;

public class PrintVowelsAtoM {

	public static void main(String[] args) {

		for (char cr = 'a'; cr <= 'm'; cr++) {
			if (cr == 'a') {
				System.out.println("Given Charactor is Vowel=" + cr);
			} else if (cr == 'e') {
				System.out.println("Given Charactor is Vowel=" + cr);
			} else if (cr == 'i') {
				System.out.println("Given Charactor is Vowel=" + cr);
			}

			//else
				//System.out.println("Given Charactor is not Vowel=" + cr);

		}
	}
}
