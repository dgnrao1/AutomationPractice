package Day12.PracticeForLoop;

public class UpperCaseAlphabet {

	public static void main(String[] args) {
		System.out.println("        Print Upper Case     ");
		for (char i = 'A'; i <= 'Z'; i++) {
			System.out.println(i);
		}
		System.out.println("Printing lower Case letters \n ");
		for (char i = 'a'; i <= 'z'; i++) {
			System.out.println(i);
		}
		System.out.println("Print Reverse z to a");
		for (char i = 'z'; i >= 'a'; i--) {
			System.out.println(i);
		}

	}

}
