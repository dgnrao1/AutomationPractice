package Day18.DoWhile;

public class Switch2Vowels {

	public static void main(String[] args) {
		char ch='b';
		switch(ch) {
	case 'a':
			System.out.println("Vowel is="+ch);
		break;
	case 'e':
		System.out.println("Vowel is="+ch);
	break;
	case 'i':
		System.out.println("Vowel is="+ch);
	break;
	case 'o':
		System.out.println("Vowel is="+ch);
	break;
	case 'u':
		System.out.println("Vowel is="+ch);
	break;
	default:
		System.out.println("Not Vowel");
		

	}

	}}
