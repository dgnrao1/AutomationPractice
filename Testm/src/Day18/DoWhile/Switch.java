package Day18.DoWhile;

public class Switch {

	public static void main(String[] args) {
		int month=3;
		switch(month) {
	case 1:
			System.out.println("Given Month Is Jan");
		break;
		case 2:
			System.out.println("Given Month Is Feb");
		break;
		case 3:
			System.out.println("Given Month Is March");
		break;
		case 4:
			System.out.println("Given Month Is April");
		break;
		case 5:
			System.out.println("Given Month Is May");
		break;
		default:
			System.out.println("No Such Mach");
			break;
		}

	}

}
