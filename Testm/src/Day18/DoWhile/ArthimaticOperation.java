package Day18.DoWhile;

public class ArthimaticOperation {

	public static void main(String[] args) {
		int a = 20, b = 10, result;
		char ch='/';
		switch (ch) {
		case '+':
			result = a + b;
			System.out.println("Additio of two num is=" + result);
			break;
		case '-':
			result = a - b;
			System.out.println("Additio of two num is=" + result);
			break;
		case '*':
			result = a * b;
			System.out.println("Additio of two num is=" + result);
			break;
		case '/':
			result = a / b;
			System.out.println("Additio of two num is=" + result);
			break;

		}

	}

}
